export function driverPrice(driver) {
  if (driver.status === 'Critical') return 0;
  if (driver.type === 'Firmware') return 19;
  if (driver.type === 'Graphics') return 29;
  return 12;
}

export function driverImage(driver) {
  const imageByType = {
    Graphics:
      'https://images.unsplash.com/photo-1591488320449-011701bb6704?q=80&w=1200&auto=format&fit=crop',
    Chipset:
      'https://images.unsplash.com/photo-1518770660439-4636190af475?q=80&w=1200&auto=format&fit=crop',
    Audio:
      'https://images.unsplash.com/photo-1511379938547-c1f69419868d?q=80&w=1200&auto=format&fit=crop',
    Network:
      'https://images.unsplash.com/photo-1544197150-b99a580bb7a8?q=80&w=1200&auto=format&fit=crop',
    Firmware:
      'https://images.unsplash.com/photo-1587202372775-e229f172b9d7?q=80&w=1200&auto=format&fit=crop',
  };

  return imageByType[driver.type] || imageByType.Graphics;
}

export function mapDriverToProduct(driver) {
  return {
    id: `driver-${driver.id}`,
    name: driver.name,
    category: driver.type,
    price: driverPrice(driver),
    originalPrice: null,
    rating: driver.status === 'Stable' ? 4.8 : driver.status === 'LTS' ? 4.7 : 4.3,
    reviews: 140 + driver.size.length * 7,
    sales: 90 + driver.version.length * 5,
    image: driverImage(driver),
    badge: driver.status,
    features: [driver.os, driver.version, driver.size],
    description: `Paquete de ${driver.type.toLowerCase()} para ${driver.os}. Versión ${driver.version} disponible para descarga segura.`,
    longDescription: `Driver ${driver.name} orientado a ${driver.type.toLowerCase()} con soporte para ${driver.os}. Tamaño del paquete: ${driver.size}. Última actualización: ${driver.date}.`,
    details: [
      `Versión: ${driver.version}`,
      `Sistema operativo: ${driver.os}`,
      `Tamaño del paquete: ${driver.size}`,
      `Estado del release: ${driver.status}`,
    ],
    includes: ['Instalador verificado', 'Checksum', 'Notas de versión'],
  };
}

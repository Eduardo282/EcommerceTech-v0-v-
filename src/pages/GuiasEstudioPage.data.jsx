export function levelPrice(level) {
  if (level === 'Avanzado') return 59;
  if (level === 'Intermedio') return 39;
  return 24;
}

export function mapGuideToProduct(guide) {
  return {
    id: `guide-${guide.id}`,
    name: guide.title,
    category: guide.category,
    price: levelPrice(guide.level),
    originalPrice: null,
    rating: guide.rating,
    reviews: guide.students,
    sales: Math.max(guide.modules * 8, 25),
    image: guide.image,
    badge: guide.level,
    features: guide.tags,
    description: guide.description,
    longDescription: `${guide.description} Incluye ${guide.modules} módulos, una duración estimada de ${guide.duration} y una ruta de aprendizaje enfocada en resultados reales.`,
    details: [
      `Nivel: ${guide.level}`,
      `Duración estimada: ${guide.duration}`,
      `${guide.modules} módulos guiados`,
      `${guide.students} estudiantes inscritos`,
    ],
    includes: ['Acceso inmediato', 'Contenido por módulos', 'Ruta práctica de estudio'],
  };
}

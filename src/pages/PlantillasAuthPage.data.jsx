export function Preview2FA() {
  const [code, setCode] = useState(['', '', '', '', '', '']);
  const handleDigit = (i, v) => {
    if (!/^\d?$/.test(v)) return;
    const next = [...code];
    next[i] = v;
    setCode(next);
  };
  return (
    <div className="bg-[#0A0A0A] rounded-b-2xl border border-white/10 p-8 shadow-2xl text-left">
      <div className="absolute top-0 left-0 w-full h-1 bg-linear-to-r from-orange-500 to-red-600" />
      <div className="text-center mb-6">
        <div className="h-14 w-14 rounded-full bg-orange-500/10 border-2 border-orange-500/30 mx-auto flex items-center justify-center mb-3">
          <Fingerprint className="text-orange-400 h-7 w-7" />
        </div>
        <h2 className="text-xl font-bold text-white mb-1">Verificación 2FA</h2>
        <p className="text-gray-400 text-xs">
          Ingresa el código de 6 dígitos de tu app autenticadora
        </p>
      </div>
      <div className="flex gap-2 justify-center mb-6">
        {code.map((d, i) => (
          <input
            key={i}
            type="text"
            maxLength={1}
            value={d}
            onChange={(e) => handleDigit(i, e.target.value)}
            className="h-12 w-10 text-center text-xl font-mono font-bold text-white bg-[#111] border-2 border-white/10 rounded-xl focus:border-orange-500 focus:outline-none transition-colors"
          />
        ))}
      </div>
      <button className="w-full bg-orange-600 hover:bg-orange-500 text-white font-bold py-3 rounded-xl text-sm transition-colors">
        Verificar código
      </button>
      <div className="mt-4 text-center text-xs text-gray-500">
        <span>¿No recibes el código? </span>
        <button className="text-orange-400 hover:text-orange-300">Reenviar en 0:42</button>
      </div>
      <div className="mt-4 flex items-center gap-2 bg-orange-500/5 border border-orange-500/20 rounded-xl p-3">
        <ShieldCheck className="h-4 w-4 text-orange-400 flex-shrink-0" />
        <p className="text-[10px] text-gray-400">
          Tu cuenta está protegida con autenticación de dos factores
        </p>
      </div>
    </div>
  );
}

